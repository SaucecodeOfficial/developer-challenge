const roundNumber = function (num, dec) {
  return Math.round(num * Math.pow(10, dec)) / Math.pow(10, dec)
}

const constrainedSize = function (WIDTH, HEIGHT, { width, height }) {
  if (width) {
    const nextHeight = width / WIDTH * HEIGHT
    return {
      width: `${width}px`,
      height: `${roundNumber(nextHeight, 2)}px`,
    }
  }
  const nextWidth = height / HEIGHT * WIDTH
  return {
    width: `${roundNumber(nextWidth, 2)}px`,
    height: `${height}px`,
  }
}

